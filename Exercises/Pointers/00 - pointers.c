// Standard variable stores standard value
// Pointer variable stores an address

// pointers declaration:
// int *p (data type "point" to int)

// <data-type> *<variable-name>
// int *p;
// double *dp;
// char *cp;

// pointers usage:
// & - Address
// * - pointer

// example:
// int a = 5;
// int *p;
// printf("%d", a); --> 5 
// printf("%p", &a); --> address of "a"  <9283823>






